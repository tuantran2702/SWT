package model;

/**
 *
 * @author Mido
 */
public class Category {
    private int id;
    private String name;
    private String image;
    private String link;
    private int parentID;  

    public Category() {
    }

    public Category(int id, String name, String image, String link, int parentID) {
        this.id = id;
        this.name = name;
        this.image = image;
        this.link = link;
        this.parentID = parentID;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public int getParentID() {
        return parentID;
    }

    public void setParentID(int parentID) {
        this.parentID = parentID;
    }
    
    
}
